    public int canCompleteCircuit(int[] gas, int[] cost) {
        ArrayList<Integer> indexes = new ArrayList<>();
        int length = gas.length;
        for(int i = 0;i<gas.length;i++){
            if(gas[i] >= cost[i]) {
                indexes.add(i);
            }
        }

        for(Integer index : indexes) {
            //index - > length;
            int sums = 0;
            int cnts = 0;
            for(int i = index;i<length;i++){
                int g = gas[i];
                int c = cost[i];
                sums+=g-c;
                cnts++;
                if(sums < 0) break;
            }
            //0->index
            for(int i = 0;i<index;i++){
                int g = gas[i];
                int c = cost[i];
                sums+=g-c;
                cnts++;
                if(sums < 0) break;
            }
            if(sums >=0 && cnts == length) return index;
        }
        return -1;
    }
